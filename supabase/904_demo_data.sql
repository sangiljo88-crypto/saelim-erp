  parts_used       text,
  cost             numeric DEFAULT 0,
  technician       text,
  result           text DEFAULT '완료',     -- 완료, 진행중, 보류
  next_check_date  date,
  recorded_by      text,
  created_at       timestamptz DEFAULT now()
