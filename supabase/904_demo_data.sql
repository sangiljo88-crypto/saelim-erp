-- 유틸리티 비용 월별 기록
CREATE TABLE IF NOT EXISTS utility_logs (
  id                 uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  log_month          text NOT NULL UNIQUE,           -- '2026-01' 형식
  electricity_kwh    numeric DEFAULT 0,
  electricity_cost   numeric DEFAULT 0,
  water_tap_ton      numeric DEFAULT 0,
  water_tap_cost     numeric DEFAULT 0,
  water_ground_ton   numeric DEFAULT 0,
  water_ground_cost  numeric DEFAULT 0,
  gas_m3             numeric DEFAULT 0,
  gas_cost           numeric DEFAULT 0,
  total_cost         numeric DEFAULT 0,
  memo               text,
  recorded_by        text,
  created_at         timestamptz DEFAULT now()
);
ALTER TABLE utility_logs DISABLE ROW LEVEL SECURITY;

-- 설비 수리 이력
CREATE TABLE IF NOT EXISTS maintenance_logs (
  id               uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  equipment_name   text NOT NULL,
  dept             text DEFAULT '생산팀',
  log_date         date NOT NULL,
  log_type         text DEFAULT '점검',     -- 점검, 수리, 교체
